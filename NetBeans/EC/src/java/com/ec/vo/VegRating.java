public class VegRating {
    private String mmRating;
    private String mcRating;
    private String pmRating;
    private String pcRating;

    public String getMcRating() {
        return mcRating;
    }

    public void setMcRating(String mcRating) {
        this.mcRating = mcRating;
    }

    public String getMmRating() {
        return mmRating;
    }

    public void setMmRating(String mmRating) {
        this.mmRating = mmRating;
    }

    public String getPcRating() {
        return pcRating;
    }

    public void setPcRating(String pcRating) {
        this.pcRating = pcRating;
    }

    public String getPmRating() {
        return pmRating;
    }

    public void setPmRating(String pmRating) {
        this.pmRating = pmRating;
    }
}
